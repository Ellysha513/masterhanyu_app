class Lesson {
  final String category;
  final String title;
  final String description;
  final int words;
  final String level;
  final double progress;
  final String imageUrl;

  Lesson({
    required this.category,
    required this.title,
    required this.description,
    required this.words,
    required this.level,
    required this.progress,
    required this.imageUrl,
  });
}
